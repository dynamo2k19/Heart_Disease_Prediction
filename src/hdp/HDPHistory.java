    private void btngoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngoActionPerformed
        DefaultTableModel tm = (DefaultTableModel)tbl.getModel();
        tm.setRowCount(0);
        try
        {
            db.rs1 = db.stm.executeQuery("SELECT Probability, Date, HID FROM hdpchkuphistory WHERE UserID = '"+tfid.getText()+"'");
            int sno = 0;
            while(db.rs1.next())
            {
                sno++;
                Object o[] = {sno, db.rs1.getInt(1), db.rs1.getDate(2), db.rs1.getInt(3)};
                tm.addRow(o);
            }
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btngoActionPerformed

