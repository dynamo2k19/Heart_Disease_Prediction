    public HDPViewFeedback(HDPFeedbackView parent, int id) {
        super(parent, true);
        initComponents();
        try
        {
            Image i = ImageIO.read(getClass().getResource("/icon.jpeg"));
            setIconImage(i);
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
        getContentPane().setBackground(new java.awt.Color(135, 187, 221));
        obfeed = parent;
        db = new DBC();
        try
        {
            db.rs1 = db.stm.executeQuery("SELECT Feedback FROM hdpfeedback WHERE FID = "+id);
            if (db.rs1.next())
            {
                tafeed.setText(db.rs1.getString(1));
            }
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
    }

