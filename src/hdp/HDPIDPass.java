    private void btncreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncreateActionPerformed
        String pass = new String(tfpsw.getPassword());
        String pass2 = new String(tfrepsw.getPassword());
        if (!pass.equals(pass2))
        {
            javax.swing.JOptionPane.showMessageDialog(null, "Password does not match", "Oops!", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            String qry = "UPDATE hdpuser SET UserID = '"+tfid.getText()+"', Password = '"+pass+"' WHERE ContactNo = '"+contact+"'";
            try
            {
                db.stm.executeUpdate(qry);
                HDPRegLog o = new HDPRegLog();
                o.setLocationRelativeTo(this);
                o.setVisible(true); 
                this.dispose();
            }
            catch (Exception e)
            {
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_btncreateActionPerformed

