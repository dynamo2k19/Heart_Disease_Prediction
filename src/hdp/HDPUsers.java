    private void loadTable()
    {
        DefaultTableModel tm = (DefaultTableModel)tbl.getModel();
        tm.setRowCount(0);
        try
        {
            db.rs1 = db.stm.executeQuery("SELECT * FROM hdpuser WHERE UserID != '"+LoginData.uid+"'");
            int sno = 0;
            while(db.rs1.next())
            {
                sno++;
                Object o[] = {sno, db.rs1.getString("UserID"), db.rs1.getString("Name"), db.rs1.getString("Age"), db.rs1.getString("Gender")
                , db.rs1.getString("Email"), db.rs1.getString("ContactNo")};
                tm.addRow(o);
            }
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
    }
