    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        if (f == 1)
        {
            String pass = new String(tfpass.getPassword());
            String qry = "SELECT UserID, Password FROM hdpuser WHERE UserID = '"+tfid.getText()+"'";
            try
            {
                db.rs1 = db.stm.executeQuery(qry);
                if (db.rs1.next())
                {
                    if (!db.rs1.getString(1).equals("admin123"))
                    {
                        JOptionPane.showMessageDialog(null, "Only Admin Can Login!!!", "UnAuthorized Access!", JOptionPane.ERROR_MESSAGE);
                    }
                    else
                    {
                        if (pass.equals(db.rs1.getString(2)))
                        {
                            new LoginData(tfid.getText());
                            new HDPAdminDash().setVisible(true);
                            this.setVisible(false);
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(null, "Incorrect Password!!!", "Try Again", JOptionPane.ERROR_MESSAGE);
                            tfpass.setText("");
                        }
                    }
                    
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "ID Not registerd!!!", "", JOptionPane.ERROR_MESSAGE);
                    tfid.setText("");
                    tfpass.setText("");
                }
            }
            catch (SQLException e)
            {
                e.printStackTrace();
            }
        }
        else
        {
            String pass = new String(tfpass.getPassword());
            String qry = "SELECT Password FROM hdpuser WHERE UserID = '"+tfid.getText()+"'";
            try
            {
                db.rs1 = db.stm.executeQuery(qry);
                if (db.rs1.next())
                {
                    if (pass.equals(db.rs1.getString(1)))
                    {
                        new LoginData(tfid.getText());
                        new HDPUserDash().setVisible(true);
                        this.setVisible(false);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Incorrect Password!!!", "Try Again", JOptionPane.ERROR_MESSAGE);
                        tfpass.setText("");
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "ID Not registerd!!!", "", JOptionPane.ERROR_MESSAGE);
                    tfid.setText("");
                    tfpass.setText("");
                }
            }
            catch (SQLException e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnloginActionPerformed

