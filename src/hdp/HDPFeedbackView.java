    private void btngoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngoActionPerformed
        try
        {
            db.rs2 = db.stm.executeQuery("SELECT * FROM hdpfeedback WHERE FID = '"+tfid.getText()+"'");
            if (db.rs2.next())
            {
                HDPViewFeedback ob = new HDPViewFeedback(this, Integer.parseInt(tfid.getText()));
                ob.setLocationRelativeTo(this);
                ob.setVisible(true);
            }
            else
            {
                javax.swing.JOptionPane.showMessageDialog(null, "No Record Found", "Try Other ID", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btngoActionPerformed

